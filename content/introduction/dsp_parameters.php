<h2 id="common">Common Parameters</h2>
<p>Common parameters that can be used across most endpoints:</p>

<ul>
	<li><strong>?wrap-response=true</strong><br/>The response json data object is wrapped in a standard container that provides the HTTP code.</li><br/>
	<li><strong>?callback=function</strong><br/>The string provided is wrapped around the response as a function call, as would be used in a JSONP response.</li>
</ul>
